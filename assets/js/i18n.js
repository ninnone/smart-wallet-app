let currentLanguage = localStorage.getItem("language") || "en";

export function setLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem("language", lang);
  updatePageTranslations();

  document.querySelectorAll("#langEN, #langFR").forEach((btn) => {
    const isActive =
      (btn.id === "langEN" && lang === "en") ||
      (btn.id === "langFR" && lang === "fr");
    if (isActive) {
      btn.classList.add('bg-primary-500', 'text-white');
      btn.classList.remove('text-secondary-700', 'hover:bg-gray-100', 'border-gray-300');
    } else {
      btn.classList.remove('bg-primary-500', 'text-white');
      btn.classList.add('text-secondary-700', 'hover:bg-gray-100', 'border', 'border-gray-300');
    }
  });
}

export function getTranslation(key) {
  return translations[currentLanguage]?.[key] || key;
}

export function getCurrentLanguage() {
  return currentLanguage;
}

function updatePageTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translation = getTranslation(key);

    if (element.tagName === "INPUT" || element.tagName === "TEXTAREA") {
      element.placeholder = translation;
    } else {
      element.textContent = translation;
    }
  });

  document.title = getTranslation('app.title');

  document.querySelectorAll('option[data-i18n]').forEach((option) => {
    const key = option.getAttribute('data-i18n');
    option.textContent = getTranslation(key);
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.getAttribute('data-i18n-placeholder');
    element.placeholder = getTranslation(key);
  });
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", init);
} else {
  init();
}

function init() {
  document.querySelectorAll("#langEN").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage("en"));
  });

  document.querySelectorAll("#langFR").forEach((btn) => {
    btn.addEventListener("click", () => setLanguage("fr"));
  });

  updatePageTranslations();
  setLanguage(currentLanguage);
}

